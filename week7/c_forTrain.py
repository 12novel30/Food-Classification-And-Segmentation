def train_model(model, params):
    num_epochs = params['num_epochs']
    optimizer = params['optimizer']
    dataloader = params['dataloader']
    dataset_sizes = params['dataset_sizes']
    device=params['device']
    scheduler = params['lr_scheduler']
    path2weights = params['path2weights']
    criterion = params['criterion']
    
    '''
    criterion = nn.CrossEntropyLoss()
    optimizer_ft = optim.SGD(model.parameters(), lr=0.001, momentum=0.9)
    exp_lr_scheduler = lr_scheduler.StepLR(optimizer_ft, step_size=7, gamma=0.1)
    '''
    loss_history = {'train': [], 'val': []}
    metric_history = {'train': [], 'val': []}
    
    best_model_wts = copy.deepcopy(model.state_dict())
    best_acc = 0.0
    
    now = datetime.now()
    print(now)
    since = time.time()
    for epoch in range(num_epochs):
        print('Epoch {}/{}'.format(epoch, num_epochs - 1))
        print('-' * 10)

        # Each epoch has a training and validation phase
        for phase in ['train', 'val']:
            print(phase) #try
            if phase == 'train':
                model.train()  # Set model to training mode
            else:
                model.eval()   # Set model to evaluate mode
            running_loss = 0.0
            running_corrects = 0
            start_time = time.time()
            for inputs, labels in dataloader[phase]:
                inputs = inputs.to(device)
                labels = labels.to(device)
                # zero the parameter gradients
                optimizer.zero_grad()
                # forward
                # track history if only in train
                with torch.set_grad_enabled(phase == 'train'):
                    outputs = model(inputs)
                    _, preds = torch.max(outputs, 1)
                    loss = criterion(outputs, labels)
                    # backward + optimize only if in training phase
                    if phase == 'train':
                        loss.backward()
                        optimizer.step()

                # statistics
                running_loss += loss.item() * inputs.size(0)
                running_corrects += torch.sum(preds == labels.data)
            if phase == 'train':
                scheduler.step()

            epoch_loss = running_loss / dataset_sizes[phase]
            epoch_acc = running_corrects.double() / dataset_sizes[phase]
            loss_history[phase].append(epoch_loss)
            metric_history[phase].append(epoch_acc)

            print('{} Loss: {:.4f} Acc: {:.4f} time: {:.4f} min'.format(phase, epoch_loss, epoch_acc, (time.time()-start_time)/60))

            # deep copy the model
            if phase == 'val' and epoch_acc > best_acc:
                best_acc = epoch_acc
                best_model_wts = copy.deepcopy(model.state_dict())
                torch.save(model.state_dict(), path2weights)
                print('!! Copied best model weights !!')
                print('!! Get best val_accuracy !!')
        
        print('=' * 10)
    
    print("="*5+"END"+"="*5)
    time_elapsed = time.time() - since
    print('Training complete in {:.0f}m {:.0f}s'
          .format(time_elapsed // 60, time_elapsed % 60))
    print('Best val Acc: {:4f}'.format(best_acc))

    model.load_state_dict(best_model_wts)
    
    return model, loss_history, metric_history

